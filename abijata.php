                              <h3>AbjiataShalla National Park</h3>
                                    <p>
Situated in the Great Rift Valley, only 200 kilometers (124 miles) south of Addis Ababa, and in the Lake Langano recreational areas, the Abijatta Shalla lakes National Park attracts numerous visitors. Using Lake Langano as your base, it is an easy trip to visit the National Park, which is 887 square kilometers in size, 482 of these being water.<br><br>

The altitude of the park ranges from 1540 to 2075 meters, the highest peak being mount Fike, situated between the two lakes. The network of tracks in this park is always developing. At present you can enter at four different points, three of which are inter connected. Approaching from Addis you first reach the Horakello entrance, where the small Horakello stream flows between lakes Langano and Abijatta.<br><br>

Abijatta-Shalla Lakes National ParkIt was created primarily for its aquatic bird life, particularly those that feed and breed on lakes Abijatta and Shalla in Large numbers. The park compresses the two lakes, the isthmus between them and a thin strip of land along the shorelines of each. Developments have been limited to a number of tracks on land, and the construction of seven outposts. While attention is focused on the water birds, the land area does contain a reasonable amount of other wildlife.<br><br>

Abijatta and Shalla are both terminal lakes but very different in nature. The surrounding area is mainly acacia woodland, some of which is very degraded by man. Lake Abijatta is a shallow pan, only fourteen metres (46 feet) deep, and its level fluctuates periodically, caused in part by human activity but often by natural phenomena as yet not fully understood. The beaches are unstable and saline, and vehicles must not venture too close as there is a very real danger of sinking.<br><br>

Abijatta-Shalla Lakes National ParkLake Shalla, by contrast, is, at 260 metres (853 feet), Ethiopia's deepest Rift Valley lake, possibly the deepest lake in Africa north of the Equator. It is an exceptionally beautiful and still largely un¬touched stretch of water, with several hot springs that bubble up by the shore and flow into the lake.<br><br>

The sides are steep and rocky — often right down to the shore. Although swimming is considered safe, it may feel strange: the water's colour is like cold tea and there is a high concentration of salts, making it feel soapy. Few fish are found in this lake.<br><br>

Abijatta-Shalla Lakes National ParkThe park was created for the many aquatic bird species that use the lakes, particularly great white pelicans and greater and lesser flamingo. Shalla’s islands are used as breeding sites by many birds, and is home to the continent's most important breeding colony of great white pelicans. Because of the lake's lack of fish, the birds fly to Lake Abijatta — which has no islands — to feed. Other birds include white-necked cormorant, African fish eagle, Egyptian geese, various plover species, and herons. Although renowned for its bird life, Abijatta is now outclassed by Lake Awasa farther to the south.<br><br>

Local mammals are not numerous but include Grant's gazelle — the northern limit for this species — greater kudu, oribi, warthog, and golden jackal.<br><br>

Unique feature:<br><br>
It has one of the highest wetland bird diversity in EthiopiaIt is home to 144 water bird species, which is in effect 70.6% of thetotal wetland bird species for the country.It is the most important breeding location for the Great White PelicanIt is known to be the best site in the country to find high number of Lesser FlamingosActs as a staging ground for thousands of migrating waders and ducks during the palaearctic migration period.Shalla is one of the deepest lakes in Africa and is reputedly one of the widest calderas on the continent.<br><br>

