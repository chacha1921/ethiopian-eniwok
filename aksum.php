Situated in the highlands of northern Ethiopia, Aksum symbolizes the wealth and importance of the civilization of the ancient Aksumite kingdom, which lasted from the 1st to the 8th centuries AD. The kingdom was at the crossroads of the three continents: Africa, Arabia and the Greco-Roman World, and was the most powerful state between the Eastern Roman Empire and Persia. In command of the ivory trade with Sudan, its fleets controlled the Red Sea trade through the port of Adulis and the inland routes of north eastern Africa.<br><br>

The ruins of the ancient Aksumite Civilization covered a wide area in the Tigray Plateau. The most impressive monuments are the monolithic obelisks, royal tombs and the palace ruins dating to the 6th and 7th centuries AD.<br><br>

Several stelae survive in the town of Aksum dating between the 3rd and 4th centuries AD. The largest standing obelisk rises to a height of over 23 meters and is exquisitely carved to represent a nine-storey building of the Aksumites. It stands at the entrance of the main stelae area. The largest obelisk of some 33 meters long lies where it fell, perhaps during the process of erection. It is possibly the largest monolithic stele  that ancient human beings ever attempted to erect. <br><br>

A series of inscription on stone tablets have proved to be of immense importance to historians of the ancient world. Some of them include trilingual text in Greek, Sabaean and Ge'ez (Classical Ethiopian), inscribed by King Ezana in the 4th century AD.<br><br>

The introduction of Christianity in the 4th century AD resulted in the building of churches, such as Saint Mary of Zion, rebuilt in the Gondarian period, in the 17th century AD, which is believed to hold the Ark of the Covenant.<br><br>

