The boundaries of the property, which encompass the entire area of ancient Aksum town, need to be adequately delineated and approved by the Committee.<br><br>

One obelisk, removed from the site and taken to Rome as a war trophy during the Italian occupation, was returned to Aksum in 2005 and re-erected in in 2008.<br><br>

Furthermore, at the time of inscription, it was noted that small, modern houses were built over most of the site, obscuring the majority of the underground Aksumite structures. Some of them still remain covered by modern houses. In 2011, the construction of a new museum began in the main Stelae Field and, unless amended, the height of the museum will have a highly negative visual impact on the property. Flooding has also become a major problem in the 4th century AD Tomb of the Brick Arches and other monuments. <br><br>

For the reasons mentioned above, the integrity of the property remains vulnerable.<br><br>

                                            <b>Authenticity</b><br><br>

The authenticity of the obelisks, tombs and other monuments remain intact, although they are vulnerable due to lack of conservation. However, the authenticity of the whole property in terms of its ability to convey the scope and extent of ancient Aksum and its value is still vulnerable to lack of documentation, delineation and lack of planning controls. The monuments need to be related to the overall city plan, in spatial terms.<br><br>

                                            <b>Protection and Management Requirements</b><br><br>

The city of Aksum was put under the jurisdiction and protection of the National Antiquities Authority in 1958. No special legal framework is provided to protect the Obelisks of Aksum, except the general law, Proclamation No. 209/2000, which also established the institution in charge, the Authority for Research and Conservation of Cultural Heritage (ARCCH).<br><br>

The property is managed at three levels – the site; the region; and the Federal administration. ARCCH prepared a proclamation that mapped and identified the precise area to be protected with local site authorities. It is reviewing the components and may wish to suggest changes to the number and/or size of the property.<br><br>

The boundary and the property’s management plan are not yet established. There is a need to submit an up-dated map of the property to clearly indicate the boundary, to produce and submit a management plan and to delineate and submit a buffer zone. There is also a need for adequate legal protection to be put in place</p>
