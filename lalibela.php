                                    <h2>Outstanding Universal Value</h2>
                                    <h3><b>Brief Synthesis</b></h3>
                                        <p>

In a mountainous region in the heart of Ethiopia, some 645 km from Addis Ababa, eleven medieval monolithic churches were carved out of rock. Their building is attributed to King Lalibela who set out to construct in the 12th century a ‘New Jerusalem’, after Muslim conquests halted Christian pilgrimages to the holy Land. Lalibela flourished after the decline of the Aksum Empire.<br><br>

There are two main groups of churches – to the north of the river Jordan: Biete Medhani Alem  (House of the Saviour of the World), Biete  Mariam (House of Mary), Biete  Maskal (House of the Cross), Biete Denagel (House of Virgins), Biete Golgotha Mikael (House of Golgotha Mikael); and to the south of the river, Biete Amanuel (House of Emmanuel), Biete Qeddus Mercoreus (House of St. Mercoreos), Biete Abba Libanos (House of Abbot Libanos), Biete Gabriel Raphael (House of Gabriel Raphael), and Biete Lehem (House of Holy Bread). The eleventh church, Biete Ghiorgis (House of St. George), is isolated from the others, but connected by a system of trenches.<br><br>

The churches were not constructed in a traditional way but rather were hewn from the living rock of monolithic blocks. These blocks were further chiselled out, forming doors, windows, columns, various floors, roofs etc. This gigantic work was further completed with an extensive system of drainage ditches, trenches and ceremonial passages, some with openings to hermit caves and catacombs. <br><br>

Biete Medhani Alem, with its five aisles, is believed to be the largest monolithic church in the world, while Biete Ghiorgis has a remarkable cruciform plan. Most were probably used as churches from the outset, but Biete Mercoreos and Biete Gabriel Rafael may formerly have been royal residences. Several of the interiors are decorated with mural paintings.<br><br>

TNear the churches, the village of Lalibela has two storey round houses, constructed of local red stone, and known as the Lasta Tukuls. These exceptional churches have been the focus of pilgrimage for Coptic Christians since the 12th century.<br><br>

                                            <b>Criterion (i):</b>All the eleven churches represent a unique artistic achievement, in their execution, size and the variety and boldness of their form<br><br>

                                            <b>Criterion (iv):</b> The King of Lalibela set out to build a symbol of the holy land, when pilgrimages to it were rendered impossible by the historical situation. In the Church of Biet Golgotha, are replicas of the tomb of Christ, and of Adam, and the crib of the Nativity. The holy city of Lalibela became a substitute for the holy places of Jerusalem and Bethlehem, and as such has had considerable influence on Ethiopian Christianity.<br><br>
                                            <b>Criterion (iii):</b> The whole of Lalibela offers an exceptional testimony to the medieval and post-medieval civilization of Ethiopia, including, next to the eleven churches, the extensive remains of traditional, two storey circular village houses with interior staircases and thatched roofs.<br><br>

<b>Integrity</b><br><br>

The drainage ditches were filled up with earth for several centuries, before being cleared in the 20th century, and have been disrupted by seismic activity. This has resulted in a severe degradation of the monuments from water damage, and most of them are now considered to be in a critical condition.<br><br>

Structural problems have been identified in Biet Amanuel where an imminent risk of collapse is possible, and other locations need to be monitored. Serious degradation of the paintings inside the churches has occurred over the last thirty years. Sculptures and bas-reliefs (such as at the entrance of Biet Mariam) have also been severely damaged, and their original features are hardly recognisable. All of this threatens the integrity of the property.<br><br>

Temporary light-weight shelters have now been installed over some churches and these, while offering protection, impact on visual integrity. <br><br>

Other threats include encroachment on the environment of the churches by new public and private construction, housing associated with the traditional village adjacent to the property, and from the infrastructure of tourism.<br><br>

                                            <b>Authenticity</b><br><br>

The Rock-Hewn Churches of Lalibela are still preserved in their natural settings. The association of the rock-hewn churches and the traditional vernacular circular houses, in the surrounding area, still demonstrate evidences of the ancient village layout. The original function of the site as a pilgrimage place still persists and provides evidence of the continuity of social practices. The intangible heritages associated with church practices are still preserved.<br><br>

                                            <b>Protection and Management Requirements</b><br><br>

For centuries, the Church and State have been jointly responsible for the holy site of Lalibela. Home to a large community of priests and monks, it is a living site which draws many pilgrims to celebrate the great feasts of the Ethiopian Christian calendar. This active and energetic perspective is central to the management of the site.<br><br>

No special legal framework is provided to protect the Rock-Hewn Churches except the general law, Proclamation No. 209/2000, which has also established the institution in charge, the Authority for Research and Conservation of Cultural Heritage (ARCCH). With the Ethiopian Church as a partner, the ARCCH has a representative in Lalibela but a principle difficulty has been the harmonization of the different projects and effective coordination between the partners.<br><br>

The property is administered under the regional and the Lasta district culture and tourism office. To prevent the property from the impact of development, a draft proclamation has been prepared but this is not yet ratified. A management plan has not yet been established. A four year Conservation Plan was established in 2006 but this has yet to be fully implemented.<br><br>
                                            There is a need for stronger planning controls for the setting of the churches that address housing, land-use tourism and for a management plan to be developed that integrates the Conservation action plan, and addresses the overall sustainable development of the area, with the involvement of the local population.

