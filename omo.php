                                    <h3>OMO National Park</h3>
                                    <p>
The Omo National Park - one of the Ethiopia's largest and richest nature sanctuary and yet one of the least visited areas in East and Central Africa. Located on the west bank of the Omo River, the park covers approximately 4,068 square kilometers, about 870 kilometers southwest of Addis Ababa. The Mago National Park, is located on the eastern bank of the Omo river. Although an airstrip was recently built near the park headquarters on the Mui River, this park is not easily reachable<br><br>
Both parks can offer incredible spectacles of big game. Both have the merit, also, of being far from the beaten track and virtually unexplored, and thus are places in which game can be seen in a truly natural state.<br><br>

Most easily accessed from the town of Jinka, Mago National Park is mainly savannah, with some forested areas around the rivers It was set up to conserve the large numbers of plains animals in the area, particularly buffalo, giraffe, and elephant. Also seen here are topi and lelwel hartebeest, as well as lion, leopard, Burchell's zebra, gerenuk, and greater and lesser kudu. OMO NATIONAL PARKThe birds are also typical of the dry grassland habitat, featuring bustards, hornbills, weavers, and starlings. Kingfishers and her-ons feed in and around the Neri River, which provides an alternative habitat.<br><br>

Although adjoining Mago, the large and beautiful Omo National Park has been hardly visited in the last two decades, as getting there has been so difficult. The only access to the park is via Omo Rate, by ferry to the west bank of the Omo River, and north to the border settlement of Kibish, where an unmaintained seventy-five-kilometre (46-mile) track leads to the Omo Park headquarters. However, the long-neglected route from Mui River up to Maji, tenuously linked to the town of Jimma, is being worked on. When this road is passable, a drive from Jimma - besides being extremely interesting in itself - will bestow the reward of visiting this truly wild and untamed area. There is virtually no tourist infrastructure within the park and little support for travellers.<br><br>

OMO NATIONAL PARKThe Omo and Mago parks are extensive wilderness areas and wildlife can be prolific: large herds of eland and buffalo, elephant, giraffe, cheetah, lion, leopard, and Burchell's zebra. Lesser kudu, lelwel hartebeest, topi, and oryx are all resident species, as well as deBrazza's and colobus monkeys and Anubis baboon. The 306 bird species recorded include many that will be familiar to East African visitors.<br><br>

The lower reaches of the Omo river were declared a UNESCO World Heritage Site in 1980, after the discovery (in the Omo Kibish Formation) of the earliest known fossil fragments of Homo sapiens, which have been dated circa 195,000 years old.<br><br>


                                    
