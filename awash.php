                                    <h3>Awash National Park</h3>
                                    <p>Awash National Park is located 225 km east of Addis Ababa, the Park stretches 30km east to west and a little less from north to south. The terrain is mainly acacia woodland and grassland.<br>

 	

 	

 
 <b>Wildlife</b><br>
At all places and all times it is possible to see game: Oryx, Soemmerring's gazelle and wild pig are common. Slightly less frequent are the furry waterbuck which tend to appear near the river in the late afternoon. The tiny dik-dik, not easy to spot in the speckled shade of the acacia thorn, zebra grazing the plains to the west of Fantale, cheetah, serval and leopard are also there but it is not easy to spot them; baboons, both anubis and hamadryas, kudus, lesser and greater, the giant tortoise, hippo, reedbuck, aardvark and caracal are also represented. Klipspringer inhabit the higher slopes of the mountain and curious hyrax peer at you curiously from behind their rocks. In the bottom of the gorge you can spot the black and white colobus monkey.<br>

<b>Birdlife</b><br>
Over four hundred species are recorded for the park: (The check list is available at the museum at park Head quarters). They range from the great ostrich, frequently and easily observed, and the less common Secretary Bird and Abyssinian Ground Hornbill, to the flashes of brilliant pink which are the Carmine Bee-eaters, and the Abyssinian Roller with turquoise and purple, wings. And between these two extremes, birds of the riverine forest, Coucal, Turaco, Go-away Birds; birds of prey; and birds of the savannah.<br>

<b>Attraction</b><br>
The park itself is traversed by a series of well-maintained tracks, which take in the most spectacular of the many scenic attractions. It is possible, and perhaps advisable, to hire a park guide.<br>
To the north at Filwoha lies the hot springs oasis in its groves of palm trees. It is reached by either one of two scenic tracks which start opposite the main gate on the far side of the road and bearing right, progress either along the floor of the Awash Falls lower Valley or along the top of the ridge.
The Awash river gorge in the south of the park has some spectacular waterfalls near the park headquarters.<br>

<b>Access</b><br>
Less than three hours' drive from Addis Ababa, or one and a half from Nazaret is the Awash National park and Game Reserve. The main entrance is at the 190 km. mark and you have already passed the park boundary as you crossed the railway track just before Fantalle Crater, which rises 600 m. from the valley floor on the left. At this point there is a track to the left and it is possible to drive either up to the crater rim or right round the park to the hot springs although the road is such that the prospect will not tempt everyone. It is probably wiser to enter the main gate first and travel comfortably down towards the Awash River which constitutes the southern boundary of the park. Here is park Headquarters, sited near the dramatic Awash falls where the river enters its gigantic gorge.<br>

<b>Accommodation</b><br>
A small bar and museum are conveniently near the camping site. The main lodge is several kilometres away across the IIala Sala plains: perched on the very rim of the gorge are several luxurious air-conditioned caravans and just on the edge, a restaurant and small swimming pool.<br>
                                    
