                                    <h3><b>Brief Synthesis</b></h3>
                                        <p>

The Lower Awash Valley paleo-anthropological site is located 300 km northeast of Addis Ababa, in the west of the Afar Depression. It covers an area of around 150 km2.<br><br>

The Awash Valley contains one of the most important groupings of paleontological sites on the African continent. The remains found at the property, the oldest of which date back over 4 million years, provide evidence of human evolution, which has modified our conception of the history of humankind. The most spectacular discovery came in 1974, when 52 fragments of a skeleton enabled the famous Lucy to be reconstructed.<br><br>

Excavations by an international team of palaeontologists and pre-historians began in 1973, and continued annually until 1976, and ended in 1980. In that time, they found a large quantity of fossilised hominid and animal bones in a remarkable state of preservation, the most ancient of which were at least four million years old. In 1974, the valley produced the most complete set of remains of a hominid skeleton, Australopithecus afarensis, nicknamed ‘Lucy’, dating back 3.2 million years. Afarensis has since been proved to be the ancestral origin for both the Genus Australopithecus and Homo-sapiens. <br><br>

A recovered female skeleton nicknamed ‘Ardi’ is 4.4 million years old, some 1.2 million years older than the skeleton of Australopithecus afarensis ‘Lucy’.<br><br>

There is a wealth of paleo-anthropological and pre-historic tools still awaiting discovery and scientific study and these are seen as constituting an exceptionally important cultural heritage resource.<br><br>

                                            <b>Criterion (i):</b>The evidence of hominid and animal fossil remains discovered in the Lower Awash Valley testify to developments in human evolution that have modified views of the history of mankind as a whole.<br><br>

                                            <b>Criterion (iv):</b>  The excavated paleo-anthropological remains from the Lower Awash Valley dating back almost 4 million years are of exceptional antiquity.<br><br>

<b>Integrity</b><br><br>

The boundaries of the sites have yet to be defined. The most extensive remains assigned were found in Hadar, one of the localities within the Lower Awash Valley, but the rest of the valley is seen to have the potential to contribute to further paleontological and historical evidence.<br><br>

Furthermore, the Middle Awash Valley has been the focus of intensive research since 1981 and it is the entire valley that is now seen to constitute one of the most important paleontological and pre-historical sites in the world. The boundaries of the property need to be defined to encompass all the attributes related to known and potential archaeological evidence. A buffer zone needs to be provided for the property.<br><br>

In spite of its remote location in the Afar Depression, the property is reportedly the target of individual tourists hunting fossil souvenirs and is thus highly vulnerable. <br><br>

                                            <b>Authenticity</b><br><br>

The material authenticity is explicit in the finds themselves. However, due to the nature of the site, it is necessary to hold the unearthed finds in the National Museum. The authenticity of the immediate settings of the finds remains largely intact as a result of its desert location, but is vulnerable to fossil hunters. In order to manifest the complete storey of the finds from this valley, it is necessary to go beyond the current boundaries. Better information on the property is still needed.<br><br>

                                            <b>Protection and Management Requirements</b><br><br>

An open site, it is naturally protected by the difficult terrain and by the local Afar population. No special legal framework is provided to protect the Lower Awash Valley, except the general law, Proclamation No. 209/2000. This also established the Authority for Research and Conservation of Cultural Heritage as the institution in charge.<br><br>

The site has no local management, and is overseen from the Afar Regional Office in Asayta, 160 km away.<br><br>

A museum has been a long-standing aim of the local authorities. One of the principal American research institutions was prepared to build it in 2004, but how it was to be staffed was not resolved.<br><br>
                                    Through the Africa 2009 programme, some expertise in training, in conservation and management was provided at a regional level. Pastoral nomads live around the property, and it has been considered that protection might be improved by involving nomadic tribal chiefs in an oversight of the large area.<br><br>
                                    There is an urgent need to re-assess and define the boundaries so as to encompass all the attributes of Outstanding Universal Value, to define a buffer zone, to put in place local protection, perhaps through the local communities, and to prepare an overall management plan that sets out how protection, management and interpretation will be met in the medium and long term.</p>
