Nechisar National Park is situated 510km south of Addis near the town of Arba Minch, in between Lakes Abaya and Chamo. From the town on the ridge of land that divides Abaya and Chamo there are commanding panoramic views all around, including both lakes with Nechisar on the eastern side and, to the west, the Guge range of mountains. The outstanding beauty of the neck of land between the two lakes has earned it the sobriquet of'Bridge of Heaven'. The equally poetic Arba Minch - meaning 'forty springs' - takes its name from the bubbling streams which spring up amid the undergrowth of the luxuriant groundwater forest that covers the flats beneath the town. This alluring area is considered one of Ethiopia's last great surviving wildernesses.<br> <br> 

Nechisar National ParkA wide variety of plains game roam freely amongst 514 km2 of savannah, dry bush and ground water forest, which are also the habitat of 188 recorded species of birds. Animals to be seen are Bushbuck, Swayne's Hartebeest, Burchell's Zebra, Grant's Gazelle, Guenther's Dik-dik, Greater Kudu, Crocodile, Anubis Baboon, Grey Duiker. Birds seen include Red-billed Hornbill, Grey Hornbil,l Fish Eagle, Kori Bustard, Abyssinian Ground Hornbill. A backdrop of hills and mountains combine to make this one of the most attractive national parks in Ethiopia, and its location makes Nechisar Park very accessible. In the far eastern part of the Nechisar park is hot springs bubble to the surface.The shores and islands of Abaya and Chamo are populated by farming peoples such as the Ganjule and the Guji, both of whom also have ancient traditions of hippo hunting. The Guji ply the Lake Abaya waters in elegantly curved high-prowed ambatch boats similar to those depicted on the tombs of Egyptian phar-aohs. Made of extremely light wood, an ambatch is capable of transporting several cattle at one time and is sufficiently sturdy to withstand any attack by crocodiles, which are present in large numbers - and large sizes - on both lakes.<br> <br> 

Nechisar National ParkNechisar National Park<br> <br> 
The vivid contrasts of the Nechisar National Park will linger long in your memory - a swathe of white grass against the backdrop of clearly defined, deeply cut hills and mountains. From the escarpment on which Arba Minch stands you look down on the clear blue waters of Lake Chamo and the sandy beaches of its northern shores, covered by crocodiles lounging in the sun.<br> <br> 

To the north of Nechisar National Park, Lake Abaya's surface is a startling contrast of dark red, caused by the suspended load of ferrous hydroxide in its waters. At the base of the escarpment is a large area of groundwater forest around the Kulfo River, as well as the 'forty springs' after which Arba Minch is named. The western edge of the Rift Valley forms an impressive backdrop to the west. Within the forest are shy, chestnut-red bushbuck, the comical bushpig, troops of Anubis baboons, and vervet monkeys.<br> <br> 

