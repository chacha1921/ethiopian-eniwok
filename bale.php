                                    <h3>Bale Mountain National Park</h3>
                                    <p>The Bale Mountains National Park is located in southeastern Ethiopia, 400km southeast of Addis Ababa and 150km east of Shashamene in the Oromia Regional National State. It belongs to the Bale-Arsi massif, which forms the western section of the southeastern Ethiopian highlands.<br>

Nominated in 2009 to UNESCO’s World Heritage Site Tentative List, the Bale Mountains are Ethiopia’s best-kept secret. Dominating the southeastern edge of the Rift Valley highlands, these mountains encompass the largest expanse of Afroalpine habitat in the world. It has been estimated that if conservation efforts in the Bale Mountains are not successful and people continue to exploit the resources in an unsustainable way, more species of mammal would become extinct than in any other area of equivalent size on the planet.<br>

Ethiopia is one of the top 25 biodiversity-rich countries in the world, and the Bale Mountains have one of the highest incidences of animal endemism of any terrestrial habitat on earth; the park itself holds 26 percent of Ethiopia’s endemic species. In addition to a wealth of endemic, unique and endangered mammals, Bale Mountains is home to a wide range of bird species. Rated by the African Bird Club as the number four birding spot in Africa, the Bale Mountains are home to over 300 species of birds – six of which are endemic to Ethiopia and 11 to Ethiopia and neighboring Eritrea.<br>

The Bale Mountains are an area of supreme importance for numerous reasons. They sustain the life of many endemic and endangered species. Additionally, over 12 million people and their livestock – in the southeast region of Ethiopia, northern Kenya and Somalia – rely on the water that originates from the Bale massif.<br>

The Bale Mountains National Park offers opportunities for unsurpassed mountain walking, horse trekking, and scenic driving. It also presents the chance to view many of the endemic mammals that reside within the park, specifically the mountain nyala and Ethiopian wolf, as well as birds such as the wattled crane and bearded vulture.<br>

However, perhaps the primary attraction of the Bale Mountains is the simple escape it offers from city life; into peace, serenity and beauty.<br>
