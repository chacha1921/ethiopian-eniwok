                                    <h3><b>Brief Synthesis</b></h3>
                                        <p>

The Lower Valley of the Omo is located in south-western Ethiopia. It extends over an area of 165 km2. The age old sedimentary deposits in the Lower Omo Valley are now world renowned for the discovery of many hominid fossils, that have been of fundamental importance in the study of human evolution.<br><br>

The Lower Omo Valley includes the Konso and Fejej paleontological research locations with sedimentary deposit going back to the plio-pleistocene period. These have produced numerous hominid and animal fossils, including fragments of Australopithecus. The deposits of human vertebrae fauna, and paleo-environmental evolution, shed light on the earliest stages of the origins and development of Homo sapiens of Africa. The discoveries of ancient stone tools in an encampment also offers evidence of the oldest known technical activities of prehistoric beings, thus making the property one of the most significant for mankind.<br><br>

To ensure Omoâ€™s position as the yardstick against which all other ancient deposits in East Africa are measured, researched evidence from the site has established bio-stratigraphical, radiometric and magneto-stratigraphical scales spanning between one and 3.5 million years. <br><br>

Since 1966, scientific research has proved that the site significantly contributes to prominent archaeological, geological, paleo-anthropological and paleo-environmental studies.<br><br>

                                            <b>Criterion (i):</b>Evidence from the Lower Omo Valley pre-historic and paleo-anthropological site have provided a unique insight into the oldest known technical activities by pre-historic beings.<br><br>

                                            <b>Criterion (iv):</b>Discoveries from the Lower Omo Valley represent exceptional developments in the domain of cultural activities in the pre-historic time.<br><br>

<b>Integrity</b><br><br>

The boundaries of the property are not adequately defined and such definition needs to be undertaken to ensure all the sites that might contribute to its Outstanding Universal Value are included. Its wider context and setting also need to be established and protected.<br><br>

Due to its very remote location, the Omo Valley is a site that is uniquely preserved for scientific research purposes. Although no development activities are foreseen in the near future, it is vulnerable to the work of petroleum companies and other plantation operating around the site, and has been at risk from pillage.<br><br>

                                            <b>Authenticity</b><br><br>

The sites where discoveries were made remain intact, as does their context. Overall the areas that might provide further evidence of early man are undisturbed.<br><br>

                                            <b>Protection and Management Requirements</b><br><br>

The property was placed under the protection of the Administration of Antiquities in 1969 through the National Law of 1968.<br><br>

No special legal framework is provided to protect the Lower Omo Valley, except for the general law, Proclamation No. 209/2000, which established the Authority for Research and Conservation of Cultural Heritage as the institute in charge. Currently the zonal and regional Information and Culture Departments perform the management functions.<br><br>

A management plan has not yet been established and, due to the extreme geographical difficulties involved, no attempt has yet been made to define the boundary of the property or its buffer zone.<br><br>
 Recently the protection of the property has become a concern as there have emerged development activities around the area. There is therefore an urgent need to put in place structured management and to define the boundaries.<br><br>
 International research expeditions are still working at the property, as an extension of the research activities started in 1976. It was recommended in 1996 that a survey should be carried out on the present state of the deposits to record any changes brought about by erosion and this still needs to be undertaken.<br><br>
 There are an unknown number of nomads living around the Omo Valley who sometimes cross the property, raising the concern of possible occasional damage. A new bridge is scheduled to be constructed in the near future, 104 km from the valley, and this will bring both benefits and threats to the property that will need to be managed.
